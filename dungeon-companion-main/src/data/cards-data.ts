export type CardCategory = "monster" | "weapon" | "artifact" | "potion" | "spell" | "boobyTrap";

export interface MonsterCard {
  category: "monster";
  id: string;
  name: string;
  count: number;
  ac: number;
  hp: number;
  move: number;
  undead?: boolean;
  description: string;
  specialAbility?: string;
  specialAbilityDetail?: string;
  dice: string[];
  rangedDice?: string[];
}

export interface WeaponCard {
  category: "weapon";
  id: string;
  name: string;
  level: number;
  type: "melee" | "ranged" | "thrown";
  description: string;
  dice: string[];
  specialAbility?: string;
  powerAttack?: boolean;
  protectsFromParalysis?: boolean;
}

export interface ArtifactCard {
  category: "artifact";
  id: string;
  name: string;
  level: number;
  description: string;
  effect: string;
  discardCondition?: string;
}

export interface PotionCard {
  category: "potion";
  id: string;
  name: string;
  level: number;
  description: string;
  effect: string;
}

export interface SpellCard {
  category: "spell";
  id: string;
  name: string;
  level: number;
  description: string;
  effect: string;
  dice: string[];
  restriction?: string;
  singleUse?: boolean;
}

export interface BoobyTrapCard {
  category: "boobyTrap";
  id: string;
  name: string;
  level: number;
  description: string;
  effect: string;
}

export type GameCard = MonsterCard | WeaponCard | ArtifactCard | PotionCard | SpellCard | BoobyTrapCard;

